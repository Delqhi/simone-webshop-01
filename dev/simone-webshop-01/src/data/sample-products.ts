export const getProductById = (id: string): Product | undefined => {
  return sampleProducts.find((p) => p.id === id)
}

export const getProductsByCategory = (categoryId: string): Product[] => {
  return sampleProducts.filter((p) => p.categoryId === categoryId)
}

export const getBestsellers = (): Product[] => {
  return [...sampleProducts].sort((a, b) => b.reviewCount - a.reviewCount).slice(0, 4)
}

export const getNewArrivals = (): Product[] => {
  return sampleProducts.filter((p) => p.isNew).slice(0, 4)
}

export const getSaleProducts = (): Product[] => {
  return sampleProducts.filter((p) => p.isSale).slice(0, 4)
}
